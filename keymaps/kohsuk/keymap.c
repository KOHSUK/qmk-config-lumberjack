/*
 * Ported from ZMK config for Ortholinix split keyboard (48 keys, 4x6x2)
 * to Lumberjack (60 keys, 5x12, ATmega328p).
 *
 * Row mapping:
 *   LJ Row 0 (num row): ESC 1-0 BSPC  (no ZMK equivalent)
 *   LJ Row 1 (= row)  : ZMK Row 0     (= Q..T / Y..P -)
 *   LJ Row 2 (TAB row): ZMK Row 1     (TAB A..G / H..SQT)
 *   LJ Row 3 (SFT row): ZMK Row 2     (LSFT Z..B / N../  RSFT)
 *   LJ Row 4 (thumb)  : ZMK Row 3     (14 keys -> 12 keys, BT keys dropped)
 *
 * Thumb cluster mapping (ZMK 14 -> LJ 12, BT_NXT/BT_PRV dropped):
 *   ZMK: LCTRL LALT LWIN MO(5) LT(2,SPC) LT(1,SPC) [BT_NXT BT_PRV] LT(3,RET) LT(4,BSPC) LT(6,SPC) RWIN RALT RCTRL
 *   LJ:  LCTL  LALT LGUI MO(5) LT(2,SPC) LT(1,SPC)                  LT(3,ENT) LT(4,BSPC) LT(6,SPC) RGUI RALT RCTL
 */

